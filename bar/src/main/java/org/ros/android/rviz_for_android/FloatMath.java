package org.ros.android.rviz_for_android;

public class FloatMath {
    private FloatMath(){};
    public static Float sqrt(Float op){
        return (float)Math.sqrt(op);
    }
    public static Float cos(Float op){
        return (float)Math.cos(op);
    }
    public static Float sin(Float op){
        return (float)Math.sin(op);
    }
}
